      FUNCTION POIJ (L,D,FG)

c     INCLUDE '../include/param.cmn'
c     INCLUDE '../include/const.cmn'

C     *
C     POIJ  ADDITIVE TERM
C     D     CHARGE SEPARATION.
C     FG    REFERENCE ONE-CENTER INTEGRAL
C     *
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      IMPLICIT INTEGER (I-N)

      PARAMETER (EPSIL=1.0D-08,  EVOLT    = 2.72113961D1)
      PARAMETER (G1=0.382D0)
      PARAMETER (G2=0.618D0)
      PARAMETER (NITER=100)
      PARAMETER
     + (ZERO =  0.0D0, ONE  =  1.0D0, TWO  =  2.0D0, THREE = 3.0D0,
     +  FOUR =  4.0D0, FIVE =  5.0D0, SIX  =  6.0D0, SEVEN = 7.0D0,
     +  EIGHT = 8.0D0,FNINE =  9.0D0, TEN  = 10.0D0, HALF =  0.5D0,
     +  THRD =  ONE  / THREE, QURT = 0.25D0)


c     write(6,*)"i am in poij"
      POIJ = ZERO
      F1 = ZERO
      F2 = ZERO
      IF(L.EQ.0) THEN
         POIJ = EVOLT*HALF/FG
      ELSEIF(L.LE.2)THEN
      DSQ    = D*D
      EV4    = EVOLT/FOUR
      EV8    = HALF*EV4
      A1     = 0.1D0
      A2     = FIVE
      DO 10 I=1,NITER
      DELTA  = A2-A1
      IF(DELTA.LT.EPSIL) GO TO 20
      Y1     = A1 + DELTA*G1
      Y2     = A1 + DELTA*G2
      IF(L.EQ.1) THEN
         F1  = (EV4*(ONE/Y1-ONE/SQRT(Y1**2+DSQ)) - FG)**2
         F2  = (EV4*(ONE/Y2-ONE/SQRT(Y2**2+DSQ)) - FG)**2
      ELSE IF(L.EQ.2) THEN
         F1  = (EV8*(ONE/Y1-TWO/SQRT(Y1**2+DSQ*HALF)
     1                     +ONE/SQRT(Y1**2+DSQ)) - FG)**2
         F2  = (EV8*(ONE/Y2-TWO/SQRT(Y2**2+DSQ*HALF)
     1                     +ONE/SQRT(Y2**2+DSQ)) - FG)**2
      ENDIF
      IF(F1.LT.F2) THEN
         A2  = Y2
      ELSE
         A1  = Y1
      ENDIF
  10  CONTINUE
C     DEFINE ADDITIVE TERM AFTER CONVERGENCE OF ITERATIONS.
  20  IF(F1.GE.F2) THEN
         POIJ = A2
      ELSE
         POIJ = A1
      ENDIF
      ENDIF
      RETURN
      END
